.INCLUDE "M32DEF.INC"

LDI R16,HIGH (RAMEND)
OUT SPH,R16
LDI R16,LOW (RAMEND)
OUT SPL,R16

.DEF DVIDED=R16
.DEF DVIDBY=R17
.DEF REST=R18

MAIN :
		LDI DVIDBY,10
		LDI DVIDED,95
		CLR REST
LOOP :  INC REST
		SUB DVIDED,DVIDBY
		BRCC LOOP

		DEC REST
		ADD DVIDED,DVIDBY

